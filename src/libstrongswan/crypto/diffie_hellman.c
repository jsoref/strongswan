/**
 * See header.
 */
void diffie_hellman_init()
{
	int i;

	/* the default exponent size set above is based on the optimal length
	 * according to RFC 3526 */
	if (lib->settings->get_bool(lib->settings,
					"%s.dh_exponent_ansi_x9_42", TRUE, lib->ns))
	{
		for (i = 0; i < countof(dh_params); i++)
		{
			/* according to RFC 5114 the size of the exponent for these DH
			 * groups should equal the size of their prime order subgroup */
			if (dh_params[i].public.subgroup.len)
			{
				continue;
			}
			dh_params[i].public.exp_len = dh_params[i].public.prime.len;
		}
	}
}

/**
 * Described in header.
 */
diffie_hellman_params_t *diffie_hellman_get_params(diffie_hellman_group_t group)
{
	int i;

	for (i = 0; i < countof(dh_params); i++)
	{
		if (dh_params[i].group == group)
		{
			return &dh_params[i].public;
		}
	}
	return NULL;
}

/**
 * See header.
 */
bool diffie_hellman_group_is_ec(diffie_hellman_group_t group)
{
	switch (group)
	{
		case ECP_256_BIT:
		case ECP_384_BIT:
		case ECP_521_BIT:
		case ECP_192_BIT:
		case ECP_224_BIT:
		case ECP_224_BP:
		case ECP_256_BP:
		case ECP_384_BP:
		case ECP_512_BP:
			return TRUE;
		default:
			return FALSE;
	}
}

/**
 * See header.
 */
bool diffie_hellman_verify_value(diffie_hellman_group_t group, chunk_t value)
{
	diffie_hellman_params_t *params;
	bool valid = FALSE;

	switch (group)
	{
		case MODP_768_BIT:
		case MODP_1024_BIT:
		case MODP_1536_BIT:
		case MODP_2048_BIT:
		case MODP_3072_BIT:
		case MODP_4096_BIT:
		case MODP_6144_BIT:
		case MODP_8192_BIT:
		case MODP_1024_160:
		case MODP_2048_224:
		case MODP_2048_256:
			params = diffie_hellman_get_params(group);
			if (params)
			{
				valid = value.len == params->prime.len;
			}
			break;
		case ECP_192_BIT:
			valid = value.len == 48;
			break;
		case ECP_224_BIT:
		case ECP_224_BP:
			valid = value.len == 56;
			break;
		case ECP_256_BIT:
		case ECP_256_BP:
			valid = value.len == 64;
			break;
		case ECP_384_BIT:
		case ECP_384_BP:
			valid = value.len == 96;
			break;
		case ECP_512_BP:
			valid = value.len == 128;
			break;
		case ECP_521_BIT:
			valid = value.len == 132;
			break;
		case CURVE_25519:
			valid = value.len == 32;
			break;
		case CURVE_448:
			valid = value.len == 56;
			break;
		case NTRU_112_BIT:
		case NTRU_128_BIT:
		case NTRU_192_BIT:
		case NTRU_256_BIT:
		case NH_128_BIT:
			/* verification currently not supported, do in plugin */
			valid = FALSE;
			break;
		case MODP_NULL:
		case MODP_CUSTOM:
			valid = TRUE;
			break;
		case MODP_NONE:
			/* fail */
			break;
		/* compile-warn unhandled groups, fail verification */
	}
	if (!valid)
	{
		DBG1(DBG_ENC, "invalid DH public value size (%zu bytes) for %N",
			 value.len, diffie_hellman_group_names, group);
	}
	return valid;
}
